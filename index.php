<H2>
<a name="linux">What is Linux?</a>
</H2>
</CENTER>
<P>
Linux (pronounced with a short i, as in Lynn, and rhyming with
'ducks'), is an "open source" clone of the Unix operating system, designed
to run on everything from handheld computers, to PCs, to mainframe computers.
"Open source" means
that, unlike other operating systems, you get the source code (actual computer
instructions) for the operating system.
<P>
For a PC, it requires minimally
an 80386 CPU and about four megabytes of RAM. But it also runs on
Macintoshes, Alphas and other hardware. It was developed by Linus
Torvalds starting in 1991, as a hobby project at the University of Helsinki
in Finland. Since then, its code has been massaged and helped along by
many volunteers over the Internet.
<P>
Linux is the fastest growing operating
system in the world. It is more robust than other popular operating
systems, and support native multi-user and multi-tasking capabilities.
Today, Linux supports almost all the features and software packages of
the many commercial versions of Unix. It supports most PC hardware, and new
devices are being supported almost daily.
<P>
Linux is a low cost solution for desktop and server environments. It's
available free for download from any number of internet sites. Or you can
purchase boxed copies (with manuals and additional "commercial" software)
from local or internet vendors all over the world. Cost is small, generally
less than other commercially available operating systems.
